export default function RoleBasedNavbar() {
  const pathname = usePathname();
  const { user, isDriver, isTeam, isRegularUser } = useUser();

  // Navigation items based on role
  const getNavItems = () => {
    if (isRegularUser) {
      // Users: home, search, favourites, notifications, profile
      return [
        { href: "/content", icon: HomeIcon, label: "Home" },
        { href: "/search", icon: SearchIcon, label: "Search" },
        { href: "/content/favourites", icon: HeartIcon, label: "Favourites" },
        {
          href: "/content/notifications",
          icon: NotificationBell,
          label: "Notifications",
        },
        { href: "/content/profile", icon: UserIcon, label: "Profile" },
      ];
    } else if (isTeam) {
      // Teams: home, create post, profile
      return [
        { href: "/content", icon: HomeIcon, label: "Home" },
        {
          href: "/content/create-post",
          icon: PlusCircleIcon,
          label: "Create Post",
        },
        { href: "/content/profile", icon: UserIcon, label: "Profile" },
      ];
    } else if (isDriver) {
      // Drivers: same as teams for now (can be customized later)
      return [
        { href: "/content", icon: HomeIcon, label: "Home" },
        {
          href: "/content/create-post",
          icon: PlusCircleIcon,
          label: "Create Post",
        },
        { href: "/content/profile", icon: UserIcon, label: "Profile" },
      ];
    }

    // Default fallback
    return [{ href: "/content", icon: HomeIcon, label: "Home" }];
  };

  const navItems = getNavItems();

  return (
    <>
      {/* Desktop navbar */}
      <nav className="hidden md:flex justify-between px-8 py-4 border-b border-white/10">
        <div className="flex items-center space-x-1">
          {user && (
            <span className="text-xs text-gray-400 bg-gray-800 px-2 py-1 rounded">
              {user.role.toUpperCase()}: {user.username}
            </span>
          )}
        </div>
        <div className="flex space-x-6">
          {navItems.map(({ href, icon: Icon, label }) => (
            <Link
              key={href}
              href={href}
              className={`flex items-center gap-2 text-sm hover:text-red-500 ${
                pathname === href ? "text-red-500" : "text-white"
              }`}
            >
              <Icon className="w-5 h-5" />
              {label}
            </Link>
          ))}
        </div>
      </nav>

      {/* Mobile tab bar */}
      <nav className="fixed bottom-0 inset-x-0 md:hidden bg-black border-t border-white/10 px-2 py-2 flex justify-between z-50">
        {navItems.map(({ href, icon: Icon, label }) => (
          <Link
            key={href}
            href={href}
            className={`flex flex-col items-center text-xs px-1 ${
              pathname === href ? "text-red-500" : "text-white"
            }`}
          >
            <Icon className="w-5 h-5" />
            <span className="text-[10px] mt-1">{label}</span>
          </Link>
        ))}
      </nav>
    </>
  );
}
